#Exercise 1: Set Union
set1 = {1, 2, 3}
set2 = {3, 4, 5}
union_set = set1.union(set2)
print(union_set)


#Exercise 2: Set Intersection
set1 = {1, 2, 3}
set2 = {3, 4, 5}
intersection_set = set1.intersection(set2)
print(intersection_set)


#Exercise 3: Set Difference
set1 = {1, 2, 3}
set2 = {3, 4, 5}
difference_set = set1.difference(set2)
print(difference_set)


#Exercise 4: Set Symmetric Difference
set1 = {1, 2, 3}
set2 = {3, 4, 5}
symmetric_difference_set = set1.symmetric_difference(set2)
print(symmetric_difference_set)


#Exercise 5: Making Data Unique with Sets
data = [1, 2, 2, 3, 3, 3, 4, 4, 4, 4]
unique_data = set(data)
print(unique_data)

#Exercise 6: Adding and Removing Elements from a Set
my_set = {1, 2, 3}
my_set.add(4)
my_set.add(5)
my_set.add(6)
my_set.remove(2)
print(my_set)

#Exercise 7: Clearing a Set
my_set = {1, 2, 3}
my_set.clear()
print(my_set)



